/* 理解“指针 = 操作变量的地址”
 * 构建“数组 = 连续内存 + 指针偏移”的理解 */

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
		i++;
	return (i);
}
