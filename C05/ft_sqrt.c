/* 数学类函数如果只靠背，是绝对记不住的。 只有当你亲手把逻辑推出来，它才会牢固地刻在脑子里。
 *
 * sqrt 的循环：
 * while (i * i < nb)
 * 你见过 N 次，但没有完全明白：
 * 为什么平方根上界不需要到 nb？
 * 为什么不从 nb/2 开始？
 * 为什么 0 和负数特殊处理？
 */
int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	while (i * i < nb)
		i++;
	if (i * i == nb)
		return (i);
	return (0);
}

