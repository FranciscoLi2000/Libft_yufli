/* 1. const：你永远不会修改 src
 * 这是工业代码中非常关键的习惯。
 * 理由：
 * 让阅读者知道 src 不会被修改
 * 让编译器帮你防止意外赋值
 * 与 libc (strcat) 的原型一致（它也用 const char *src）*/
char	*ft_strcat(char *dest, const char *src)
{
	size_t	d;
	size_t	s;

	d = 0;
	s = 0;
	while (dest[d] != '\0')
		d++;
	while (src[s] != '\0')
		dest[d++] = src[s++];
	dest[d] = '\0';
	return (dest);
}

/* 2. 用 size_t 而不是 int
 * 因为：
 * size_t 是用来计数字节长度的官方类型
 * 在 64 位系统上，int 可能不足
 * 和所有标准库（strlen、strcmp、strcat）一致 */

/* 3. 名字可更语义化
 * i1 和 i2 不利于阅读。
 * 更直观的写法：
 * size_t  d;
 * size_t  s;
 * 或更清晰：
 * size_t  dest_i;
 * size_t  src_i;
 * 可读性立刻提升。*/

